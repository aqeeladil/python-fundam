

# A hash map is essentially an implementation of a hash table. It is a collection of 
# key-value pairs, where each key is unique. In Python, a hash map is represented by the 
# dict type, which is implemented as a hash table under the hood.

hash_map = {}
hash_map['name'] = 'John Doe'
hash_map['age'] = 30

# Accessing values
print(hash_map['name'])  # Output: John Doe
print(hash_map['age'])   # Output: 30


